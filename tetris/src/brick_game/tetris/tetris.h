typedef enum {
  Start,
  Pause,
  Terminate,
  Left,
  Right,
  Up,
  Down,
  Action,
  Drop
} UserAction_t;

typedef struct {
  int** field;
  int** next;
  int score;
  int high_score;
  int level;
  int speed;
  int pause;
} GameInfo_t;

typedef enum {
  STATE_START,  // Определяет начальное состояние игры (ждет нажатия для старта)
  STATE_SPAWN,  // Состояние спавна новой тетромино
  STATE_MOVING,  // Состояние, когда тетромино движется вниз
  STATE_SHIFTING,  // Состояние ускоренного падения тетромино
  STATE_ATTACHING,  // Состояние прикрепления тетромино к матрице
  STATE_GAME_OVER,
  STATE_PAUSE  // Состояние окончания игры
} GameState;  // Перечисление для управления состояниями игры

typedef struct {
  GameState state;  // Текущее состояние игры (одно из значений GameState)
  int matrix[20][10];  // Матрица 20x10, представляющая игровое поле (1 -
                       // занято, 0 - свободно)
  int current_piece[4][4];  // Текущая тетромино (4x4 матрица, где 1 - часть
                            // фигуры, 0 - пусто)
  int next_piece[4][4];  // Следующая тетромино, ожидающая спавна
  int piece_x;  // Текущая координата X текущей тетромино на поле
  int piece_y;  // Текущая координата Y текущей тетромино на поле
  bool game_active;  // Флаг активности игры (true - игра идет, false -
                     // остановлена)
  long last_update;  // Время последнего обновления в миллисекундах для контроля
                     // скорости падения
  int drop_speed_ms;  // Интервал в миллисекундах между падениями тетромино (чем
                      // меньше, тем быстрее)
  int score;          // Текущий счет игрока
  int lines_cleared;  // Количество очищенных линий
  int level;  // Текущий уровень игры (влияет на скорость)
  int current_tetromino;  // Индекс текущей тетромино (0-6, соответствует 7
                          // типам фигур)
  int current_rotation;  // Текущая ориентация текущей тетромино (0-3)
  int next_rotation;  // Ориентация следующей тетромино (0-3)
  int next_tetromino;  // Индекс следующей тетромино (0-6)
  long last_move_time;  // Время последнего движения
  long game_over_start_time;  // Время начала состояния GAME_OVER
  int current_color;  // Индекс цветной пары для текущей фигуры
  int next_color;  // Индекс цветной пары для следующей фигуры
} TetrisGame;  // Структура для хранения состояния игры

